
public class Addition {
	int result=0;
	public int add(int a, int b) {
		
	return result =a+b;
	}
	
	public int sub(int b, int c) {
		
		return result =b-c;
	}
		
		public int mul(int x, int y) {
			
			return result =x*y;
		}

			public int div(int u, int z) {
				
				return result =u/z;
			}
						
				
	public static void main(String[]arg) {	
		Addition addition=new Addition();
		
	System.out.println(addition.add(10, 10));
	System.out.println(addition.sub(23,54));
	System.out.println(addition.mul(12, 4));
	System.out.println(addition.div(24,2 ));
	
				
				


}

}
